                          <p>

<p>Qualifications in this group provide a number of Core Essential Skills that are ideally suited to supporting your application to become a Certificate in Technical Accounting. These qualifications provide the foundation of skills that you will need in order to progress to the next Professional Level.</p>
<p>Certificate in Technical Accounting will typically be regarded as competently qualified to manage a set of accounting books up to and including Trial Balance. They may well be employed in various sized organisations as junior accountants, and find themselves in charge of small sections of accounting clerks in medium to large companies, or they may be self-employed book-keepers preparing the basic accounts for very small businesses and other self-employed persons.</p>
<p>&nbsp;</p>
<p>Level Descriptor</p>
<p>&nbsp;</p>
<p>Achievement at this level reflects the ability to identify and use relevant understanding, methods and skills to address problems that are well defined but complex and non-routine. It includes taking responsibility for overall courses of action as well as exercising autonomy and judgement within fairly broad parameters. It also reflects understanding of different perspectives or approaches within an area of study or work.</p>
