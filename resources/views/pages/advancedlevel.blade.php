<p>&nbsp;</p>
<p>Qualifications in this group provide a number of Core Essential Skills that you can add to those you have gained at Fundamental Level. They are ideally suited to supporting your application to become a Certificate in Advanced Technical Accounting. These qualifications further enhance your portfolio of skills that you require in order to progress to becoming a Certificate in Project or Corporate Accounting</p>
<p>A Certificate in Advanced Technical Accounting will be regarded as more senior than Technical Accounting and will be further qualified in aspects of Law, IT, Audit, and Taxation. They may typically find themselves employed in various sized organisations as more experienced junior accountants, perhaps in charge of small departments, or specialising in particular reporting roles. Again, they may be self-employed book-keepers, perhaps employing other book-keepers within their business, and preparing the basic accounts for small businesses and other self-employed persons.</p>
