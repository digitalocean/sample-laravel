                    <div class="panel-body">

                    		<p>In addition to the six professional grades listed above, there is the entry grade of Affiliate Member. The seven grades of membership within with Nazlawi Business College are explained below:</p>
<p><strong>Affiliate Member (AMNBC)</strong></p>
<p><img style="width: 350px; float: right;padding:10px;" src="images/AI_Ljubljana-University.jpg" />Professional Accountants and students considering a career as a Specialist Accountant can join Nazlawi Business College as an Affiliate Member. Affiliate membership is open to any adult who can be regarded as a "fit and proper person". Affiliate members are entitled to use the designation "AMNBC" and describe themselves as an "Affiliate Member of Nazlawi Business College ". Upon completion of the registration process Affiliate members will then have access to all the facilities offered to members, which includes gaining NBC&reg; qualifications and applying for other professional membership grades.</p>
<p><strong>Graduate Accounting Technician (GAT)</strong></p>
<p><img style="width: 350px; float: left;padding:10px;" src="images/AI_Liverpool-Ship.jpg" />The Graduate Accounting Technician is the Fundamental grade of professional membership, and the criteria for awarding this grade is significantly more involved than that for Affiliates. Graduate Accounting Technicians will typically be regarded as competently qualified to manage a set of accounting books up to and including Trial Balance. They may well be employed in various sized organisations as junior accountants, and find themselves in charge of small sections of accounting clerks in medium to large companies, or they may be self-employed book-keepers preparing the basic accounts for very small businesses and other self-employed persons. Achievement at this level reflects the ability to identify and use relevant understanding, methods and skills to address problems that are well defined but complex and non-routine. It includes taking responsibility for overall courses of action as well as exercising autonomy and judgement within fairly broad parameters. It also reflects understanding of different perspectives or approaches within an area of study or work. Graduate Accounting Technicians must have achieved a minimum combination of Learning &amp; Experience totalling at least 3,200 Spectrum Hours before they can be awarded the "GAT Graduate Accounting Technician with NBC" Professional Membership Qualification.</p>
<p><strong>Advanced Accounting Technician (AAT)</strong></p>
<p><img src='images/AI_Balance.jpg' style="width:250px; padding:10px; float:left;"/>Qualified Accounting Technicians are regarded as more senior than Graduate Accounting Technicians and will be further qualified in their Learning &amp; Experience. They may typically find themselves employed in various sized organisations as more experienced junior accountants, perhaps in charge of small departments, or specialising in particular reporting roles. Again, they may be self-employed book-keepers, perhaps employing other book-keepers within their business, and preparing the basic accounts for small businesses and other self-employed persons. Achievement at this level reflects the ability to identify and use relevant understanding, methods and skills to address broadly-defined, complex problems. It includes taking responsibility for planning and developing courses of action as well as exercising autonomy and judgement within broad parameters. It also reflects understanding of different perspectives, approaches or schools of thought and the reasoning behind them. Qualified Accounting Technicians must have achieved a minimum combination of Learning &amp; Experience totalling at least 6,400 Spectrum Hours before they can be awarded the "AAT Advanced Accounting Technician with NBC" Professional Membership Qualification.</p>
<p><strong>Associate Certified Project Accountant (ACPA)</strong></p>
<p>The Certified Project Accountant ACPA &ndash; is regarded as a Fully Academically Qualified Professional Accountant specialising in all aspects of Project and Programme Accounting and Management operating at the professional level. They are more senior than an Advanced Accounting Technician and will be further qualified in Advanced Accountancy, Project Management, and Project Accounting. He or She will be competently qualified to manage the financial and management aspects of Projects and Programmes within all organisations. Although qualified to manage projects as a Project Manager he or she will primarily be focused on the financial control and reporting of those projects. They may typically find themselves employed in large organisations in business critical positions of great importance and responsible for project portfolios worth hundreds of millions of dollars. They may also find themselves as consultants specialising in Project Accounting and Project Management.</p>
<p>&nbsp;</p>
<p><strong>Associate Certified Corporate Accountant (ACCEA)</strong></p>
<p>The Certified Corporate Accountant ACCEA - is regarded as a Fully Academically Qualified Professional Accountant specialising in all aspects of Corporate Accounting operating at the professional level. They are more senior than an Advanced Accounting Technician and will be further qualified in Advanced Accountancy, Performance Management, Decision Making, and Risk and Control Management. He or She will be competently qualified to manage the financial and management aspects of organisations of all sizes. They may typically find themselves employed in large organisations in business critical positions of great importance or find themselves working as consultants specialising in all aspects of Corporate Accounting matters.</p>
<p>&nbsp;</p>
<p>The Certified Project Accountant ACPA &amp; Certified Corporate Accountant ACCEA are a Fully Qualified Professional Accountant operating at the professional level. They are more senior than Advanced Accounting Technicians and are competently qualified to manage the financial and management aspects of organisations of all sizes. They may typically find themselves employed in large organisations in business critical positions of great importance or working as consultants specialising in all aspects of accounting matters. Achievement at this level reflects the ability to refine and use relevant understanding, methods and skills to address complex problems that have limited definition. It includes taking responsibility for planning and developing courses of action that are able to underpin substantial change or development, as well as exercising broad autonomy and judgement. It also reflects an understanding of different perspectives, approaches or schools of thought and the theories that underpin them. ACPA &amp; ACCEA must have achieved a minimum combination of Learning &amp; Experience totalling at least 9,600 Spectrum Hours before they can be awarded the "ACPA &amp; ACCEA Certified&nbsp; Accountant with NBC&reg;" Professional Membership Qualification.</p>
<p><strong>Fellow Certified Project Accountant (FCPA)</strong></p>
<p>The Fellow Certified Project Accountant FCPA &ndash;- is regarded as a Fully Academically Qualified Professional Accountant specialising in all aspects of Project and Programme Accounting and Management operating at the strategic level. They are more senior than Certified&nbsp; project Accountant ACPA and will be further qualified in Strategic Business Planning, Strategic Financial Planning, and Strategic ICT Planning. They will be competently qualified to manage the financial, management, and strategic aspects of organisations of any size, and in particular of those organisations which are primarily project orientated. They may typically find themselves employed in large organisations in business critical positions normally at board level or find themselves working as senior consultants specialising in all aspects of Project Accounting and Project Management.</p>
<p>&nbsp;</p>
<p><strong>Fellow Certified Corporate Accountant (FCCEA)</strong></p>
<p>The Fellow&nbsp; Corporate&nbsp; Accountant FCCEA &nbsp;&ndash;- is regarded as a Fully Academically Qualified Professional Accountant specialising in all aspects of Corporate&nbsp; and Programme Accounting and Management operating at the strategic level. They are more senior than Advanced corporate Accounting and will be further qualified in Strategic Business Planning, Strategic Financial Planning, and Strategic ICT Planning. They will be competently qualified to manage the financial, management, and strategic aspects of organisations of any size, and in particular of those organisations which are primarily project orientated. They may typically find themselves employed in large organisations in business critical positions normally at board level or find themselves working as senior consultants specialising in all aspects of Corporate Accounting</p>
<p>The Fellow Certified Project and Certified Corporate Accountant FCPA, FCCEA are a Fully Qualified Professional Accountant operating at the Strategic level. They are more senior than ACPA &amp; ACCEA and are competently qualified to manage the financial, management, and strategic aspects of organisations of any size. They may typically find themselves employed in large organisations in business critical positions normally at board level or working as partners and senior consultants specialising in all aspects of accounting and business matters. Achievement at this level reflects the ability to reformulate and use relevant understanding, methodologies and approaches to address problematic situations that involve many interacting factors. It includes taking responsibility for planning and developing courses of action that initiate or underpin substantial change or development, as well as exercising broad autonomy and judgement. It also reflects an understanding of relevant theoretical and methodological perspectives, and how they affect their area of study or work. FCPA &amp; FCCEA&nbsp; Accountants must have achieved a minimum combination of Learning &amp; Experience totalling at least 15,600 Spectrum Hours before they can be awarded the "FCPA , FCCEA Certified Accountant with NBC" Professional Membership Qualification.</p>
