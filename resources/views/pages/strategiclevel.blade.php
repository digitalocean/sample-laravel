                  <p><strong>Advanced Project Accountants </strong>
<p>&nbsp;</p>
<p>The Advanced&nbsp; Project Accountant &ndash;- is regarded as a Fully Academically Qualified Professional Accountant specialising in all aspects of Project and Programme Accounting and Management operating at the strategic level. They are more senior than Advanced project Accounting and will be further qualified in Strategic Business Planning, Strategic Financial Planning, and Strategic ICT Planning. They will be competently qualified to manage the financial, management, and strategic aspects of organisations of any size, and in particular of those organisations which are primarily project orientated. They may typically find themselves employed in large organisations in business critical positions normally at board level or find themselves working as senior consultants specializing in all aspects of Project Accounting and Project Management.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p>&nbsp;<strong>Advanced Corporate Accountants</strong></p>
<p>&nbsp;</p>
<p>The Advanced&nbsp; Corporate&nbsp; Accountant &ndash;- is regarded as a Fully Academically Qualified Professional Accountant specialising in all aspects of Corporate&nbsp; and Programme Accounting and Management operating at the strategic level. They are more senior than Advanced corporate Accounting and will be further qualified in Strategic Business Planning, Strategic Financial Planning, and Strategic ICT Planning. They will be competently qualified to manage the financial, management, and strategic aspects of organisations of any size, and in particular of those organisations which are primarily project orientated. They may typically find themselves employed in large organisations in business critical positions normally at board level or find themselves working as senior consultants specialising in all aspects of Corporate Accounting.</p>
<p>Qualifications in this group provide a number of Core Essential Skills that you can add to those you have gained at Levels for fundamental, advanced and professional. They are ideally suited to supporting your application to become an Advanced Project &amp; Corporate Accounting, and will further enhance your portfolio of skills in order to assure your progress towards becoming an Advanced Project &amp; Corporate Accounting.</p>
<p>The Advanced Project &amp; Corporate Accounting are a Fully Qualified Professional Accountant operating at the Strategic level. They are competently qualified to manage the financial, management, and strategic aspects of organisations of any size. They may typically find themselves employed in large organisations in business critical positions normally at board level or working as partners and senior consultants specialising in all aspects of accounting and business matters.</p>
<p>Level Descriptor</p>
<p>Achievement at this level reflects the ability to reformulate and use relevant understanding, methodologies and approaches to address problematic situations that involve many interacting factors. It includes taking responsibility for planning and developing courses of action that initiate or underpin substantial change or development, as well as exercising broad autonomy and judgement. It also reflects an understanding of relevant theoretical and methodological perspectives, and how they affect their area of study or work.</p>
<p>Note: The syllabus for is common to both Advanced Project Accountants and Advanced Corporate Accountants</p>
